"""
База данных топлив для расчёта КПД котла обратным балансом.

Источник табличных данных: "Тепловой расчет котельных агрегатов. Нормативный метод"
(Кузнецов Н.В. и др., ВТИ/ЦКТИ, 1973), таблица 3 (состав рабочей массы топлива,
теплотехнические характеристики) — воспроизведена по учебному пособию Смородина С. и др.
(СПбГТУРП), сохранённому в analysis/methodology_source.pdf проекта КПД Таштагол.

Состав рабочей массы указан в % (Wr, Ar, Sr, Cr, Hr, Nr, Or), Qir в МДж/кг (низшая
теплота сгорания рабочей массы). Vvo, VRO2, VNo2, VH2Oo, Vgo - теоретические объёмы
(м3/кг) при альфа=1, взяты из таблицы напрямую (для контроля/резерва); в расчёте они
всё равно пересчитываются из состава по формулам методики, чтобы корректно работать
при ручном изменении Wr/Ar/Qir оператором (лабораторный анализ топлива на каждом опыте).
"""

from dataclasses import dataclass, field


@dataclass
class SolidLiquidFuel:
    """Твёрдое или жидкое топливо: состав рабочей массы, % (кроме Qir)."""
    name: str
    category: str  # "Твёрдое" | "Жидкое (мазут)"
    Wr: float
    Ar: float
    Sr: float
    Cr: float
    Hr: float
    Nr: float
    Or: float
    Qir_MJ: float  # низшая теплота сгорания рабочей массы, МДж/кг (справочное; можно ввести измеренное)


@dataclass
class GasComponent:
    """Объёмная доля компонента газового топлива, % (сухой газ)."""
    CH4: float = 0.0
    C2H6: float = 0.0
    C3H8: float = 0.0
    C4H10: float = 0.0
    C5H12: float = 0.0
    H2: float = 0.0
    H2S: float = 0.0
    CO: float = 0.0
    CO2: float = 0.0
    N2: float = 0.0
    O2: float = 0.0


@dataclass
class GasFuel:
    name: str
    category: str  # "Газообразное"
    comp: GasComponent
    Qir_MJ_m3: float  # низшая теплота сгорания, МДж/м3


